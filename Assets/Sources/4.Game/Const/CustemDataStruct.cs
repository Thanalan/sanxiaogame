using System.Collections;
using System.Collections.Generic;
using UnityEngine;

namespace Game
{
    /// <summary>
    /// 自己的二维坐标
    /// </summary>
    public struct CustomVector2
    {
        public int x;
        public int y;

        public CustomVector2(int xValue,int yValue)
        {
            x = xValue;
            y = yValue;
        }
    }

}

